package dasar;

public class WhileLoop {
    public static void main(String[] args) {

        int angka = 1;
        int angka2 = 10;


        while (angka <= 10){
            System.out.println(angka);
            angka++;
        }

        System.out.println();

        while (angka2 >= 1){
            System.out.println(angka2);
            angka2--;
        }





    }
}
