package dasar;

public class DoWhileLoop {
    public static void main(String[] args) {

        int counter = 1;
        int counter2 = 1;


        do {
            System.out.println(counter);
            counter++;
        } while (counter <=10);


        System.out.println();

        do {
            System.out.println(counter2);
            counter2--;
        }while (counter2 > 1);





    }
}
